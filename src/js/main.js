'use strict';

/*----------------------------------------------------------------------------*\
    $LINKS
\*----------------------------------------------------------------------------*/

var links = {
    init: function(){
        $('a[rel*=external]').on('click', function(){
            $(this).attr('target', '_blank');
        });
    }
};





/*----------------------------------------------------------------------------*\
    $CYCLE
\*----------------------------------------------------------------------------*/

var cycle = {
    init: function(){
        var $containers = $('.js--cycle');

        $containers.each(function(){
            var $el = $(this);
            $el.cycle();
        });
    }
};





/*----------------------------------------------------------------------------*\
    $INIT
    Fire ALL the things
\*----------------------------------------------------------------------------*/

$(document).ready(function(){
    cycle.init();
    links.init();
});